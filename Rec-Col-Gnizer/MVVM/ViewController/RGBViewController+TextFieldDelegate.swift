import UIKit
import Foundation

extension RGBViewController: UITextFieldDelegate {
    func textFieldDidEndEditing(_ textField: UITextField) {
        let text = vm.clangTextField(text: textField.text)
        textField.text = text
        guard let value = Float(text) else { return }
        
        if textField == redField {
            redSlider.value = value
            vm.selectedRed = value
        } else if textField == greenField {
            greenSlider.value = value
            vm.selectedGreen = value
        } else if textField == blueField {
            blueSlider.value = value
            vm.selectedBlue = value
        }
    }
}
